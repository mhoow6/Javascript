// Navigator
// 브라우저의 정보를 제공하는 객체

// 객체의 모든 프로퍼티를 열람
console.dir(navigator);

// console.log는 요소를 HTML과 같은 트리 구조로 출력합니다.
// console.dir은 요소를 JSON과 같은 트리 구조로 출력합니다.

console.dir(navigator.appVersion);

// 브라우저가 서버에게 전달하는 정보 .appVersion과 비슷함
console.dir(navigator.userAgent);

// 브라우저가 동작하고 있는 운영체제 정보
console.dir(navigator.platform);